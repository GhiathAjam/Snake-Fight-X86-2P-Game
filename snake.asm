draw_pwr PROC FAR                                  ; Poison -- 0        Freeze -- 1          Instant_Death -- 2         Instant_Kill -- 3       Random -- 4  

        mov ax,frst
        and ax,ax
        jz erase_init
        mov frst,0
        jmp draw_pwr_init
   
        erase_init:
        ;       erasing old power
        mov al,prv_pwr
        cmp al,2
        jl erase_not_dth

        ;       erasing old death
        mov SquareWidth,8      
        xor al,al
        mov cx,dth_pwr_X
        mov dx,dth_pwr_Y
        sub cx,5
        sub dx,5
        call drawSqr
        jmp draw_pwr_init

        erase_not_dth:
        ;       erasing old power
        mov cx,pwr_X
        mov dx,pwr_Y
        dec dx
        dec cx
        mov SquareWidth,4      
        xor al,al
        call drawSqr

        draw_pwr_init:
        ;       generating rnd power
        ;       MAKE AX ALL RANDOM
        xor ax, ax
        int 1ah			;get random value from the ticks of the clock cx:dx
        mov pwr_timer,dx
        mov ax,dx
        ;       generating X from 0 to 4
        mov dx, 00h
        mov bx, 5d 	        ;the width of the screen
        div bx			;mod the random value with the width to get a random number from 0 to 38
        ; dl now has from 0 to 4        / currently two cuz others not yet drawn
        push dx
        mov prv_pwr,dl      

        cmp dl,4
        jz pwr_rnd
        cmp dl,2
        jz pwr_dth
        cmp dl,3
        jz pwr_kill
        cmp dl,1
        jz pwr_frz

        pwr_poison:
        lea di,Img_Poison
        mov pwr_img_hw,9d
        add pwr_timer, 02FH
        jmp PL1

        pwr_rnd:
        lea di,Img_Rnd
        mov pwr_img_hw,17d
        add pwr_timer, 02FH
        jmp PL1

        pwr_dth:
        lea di,Img_Dth
        mov pwr_img_hw,17d
        add pwr_timer, 06BH
        jmp PL1

        pwr_kill:
        lea di,Img_Kil
        mov pwr_img_hw,17d
        add pwr_timer, 01FH
        jmp PL1

        pwr_frz:
        lea di,Img_frz
        mov pwr_img_hw,9d
        add pwr_timer, 06FH

        PL1:
        push di
        mov pwr_img,di
        call random_x_y
        pop di
        sub cx,3d
        sub dx,3d

        pop bx
        cmp bl,2
        jl pwr_not_dth_draw
        ; check if all squares available for death 2
        push di
        call Big_random_x_y
        pop di
        mov dth_pwr_X,cx
        mov s_pwr_X,cx
        mov dth_pwr_Y,dx
        mov s_pwr_Y,dx
        jmp pwr_dth_draw

        pwr_not_dth_draw:
        mov pwr_X,   cx
        mov s_pwr_X,cx
        mov pwr_y,   dx
        mov s_pwr_Y,dx
        mov pwr_img_hw,9d
        mov imgw,9
        mov imgh,9
        call draw_image
        jmp pwr_eee

        pwr_dth_draw:
        mov pwr_img_hw,17d
        mov imgw,17d
        mov imgh,17d
        call draw_image

        pwr_eee:

        RET
draw_pwr ENDP

