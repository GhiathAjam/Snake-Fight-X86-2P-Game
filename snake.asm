Sendchar        PROC    Far     ;put in bl - puts 0 in bp if fail

        push ax
        push dx

        ;Check that Transmitter Holding Register is Empty
        mov dx , 3FDH ; Line Status Register
        fffail:      
        In al , dx ;Read Line Status
        and al , 00100000b
        JZ fffail 

        mov al,bl
        mov dx , 3F8H ; Transmit data register
        out dx , al

        pop dx
        pop ax

                ret
Sendchar        ENDP

