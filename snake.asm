                dec food_phase_size
        jz draw_food_ENDING
                mov bx, food_phase_num
                add bx, 2
                mov food_phase_num, bx
                mov di, [bx]
                mov cx, food_temp_cx
                mov dx, food_temp_dx
        jmp draw_food_Drawit

	draw_food_ENDING:
                        RET
